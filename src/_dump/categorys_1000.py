for parent_name, child_names in category_tree.items():
    parent, _ = Categories.objects.get_or_create(name=parent_name, parent=None)

    # Переводы родителя
    for code, _ in LANGUAGES:
        if code != "ru":  # русское название — оригинал
            translated = translator.translate(parent_name, src="ru", dest=code).text
            setattr(parent, f"name_{code}", translated)
    parent.save()

    for child_name in child_names:
        child, _ = Categories.objects.get_or_create(name=child_name, parent=parent)

        for code, _ in LANGUAGES:
            if code != "ru":
                translated = translator.translate(child_name, src="ru", dest=code).text
                setattr(child, f"name_{code}", translated)
        child.save()
