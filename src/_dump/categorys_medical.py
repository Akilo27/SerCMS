# 🔧 Функция для генерации уникального slug
def get_unique_slug(base):
    base_slug = slugify(base)
    slug = base_slug
    counter = 1
    while Categories.objects.filter(slug=slug).exists():
        slug = f"{base_slug}-{counter}"
        counter += 1
    return slug


